/**
 * Fetches real Google reviews using the Places API (New).
 *
 * Usage:
 *   GOOGLE_PLACES_API_KEY=your_key node scripts/fetch-google-reviews.mjs
 *
 * Or set the key in .env.local:
 *   GOOGLE_PLACES_API_KEY=AIza...
 *
 * To find your Place ID:
 *   1. Go to https://developers.google.com/maps/documentation/places/web-service/place-id-finder
 *   2. Search for your business
 *   3. Copy the Place ID
 *
 * This script saves reviews to src/data/googleReviews.ts
 * Run it weekly via cron or manually to keep reviews fresh.
 */

import fs from 'fs';
import path from 'path';

const API_KEY = process.env.GOOGLE_PLACES_API_KEY;

if (!API_KEY) {
  console.error('❌ Set GOOGLE_PLACES_API_KEY environment variable');
  console.error('   GOOGLE_PLACES_API_KEY=your_key node scripts/fetch-google-reviews.mjs');
  process.exit(1);
}

// Add your Place IDs here
const LOCATIONS = [
  {
    id: 'chandigarh',
    label: 'Chandigarh HQ',
    placeId: process.env.CHANDIGARH_PLACE_ID || 'ChIJcxIu82HtDzkRyAW7GkhFwEE',
  },
  {
    id: 'edmonton',
    label: 'Edmonton Office',
    placeId: process.env.EDMONTON_PLACE_ID || 'ChIJozGGr-0joFMRMSWW5nYx_ew',
  },
];

async function fetchPlaceDetails(placeId) {
  const fields = 'displayName,rating,userRatingCount,reviews';
  const url = `https://places.googleapis.com/v1/places/${placeId}?fields=${fields}&languageCode=en`;

  const res = await fetch(url, {
    headers: {
      'X-Goog-Api-Key': API_KEY,
      'X-Goog-FieldMask': fields,
    },
  });

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Places API error for ${placeId}: ${res.status} ${err}`);
  }

  return res.json();
}

async function main() {
  const allReviews = [];
  const locationMeta = [];

  for (const loc of LOCATIONS) {
    if (loc.placeId.startsWith('REPLACE')) {
      console.warn(`⚠️  Skipping ${loc.label} — Place ID not set`);
      continue;
    }

    console.log(`Fetching reviews for ${loc.label} (${loc.placeId})...`);
    try {
      const data = await fetchPlaceDetails(loc.placeId);
      const rating = data.rating || 0;
      const count = data.userRatingCount || 0;
      const reviews = (data.reviews || []).map((r) => ({
        author: r.authorAttribution?.displayName || 'Anonymous',
        rating: r.rating || 5,
        text: r.text?.text || '',
        date: r.publishTime || new Date().toISOString(),
        profilePhoto: r.authorAttribution?.photoUri || '',
        location: loc.id,
      }));

      locationMeta.push({
        id: loc.id,
        label: loc.label,
        placeId: loc.placeId,
        rating,
        reviewCount: count,
      });

      allReviews.push(...reviews);
      console.log(`✅ ${loc.label}: ${rating}⭐ (${count} reviews), fetched ${reviews.length} review texts`);
    } catch (err) {
      console.error(`❌ ${loc.label}:`, err.message);
    }
  }

  // Calculate combined rating
  const totalReviews = locationMeta.reduce((sum, l) => sum + l.reviewCount, 0);
  const weightedRating = locationMeta.reduce((sum, l) => sum + l.rating * l.reviewCount, 0) / (totalReviews || 1);

  const output = `// Auto-generated by scripts/fetch-google-reviews.mjs
// Last updated: ${new Date().toISOString()}
// Run: GOOGLE_PLACES_API_KEY=your_key node scripts/fetch-google-reviews.mjs

export interface GoogleReview {
  author: string;
  rating: number;
  text: string;
  date: string;
  profilePhoto: string;
  location: string;
}

export interface LocationReviewMeta {
  id: string;
  label: string;
  placeId: string;
  rating: number;
  reviewCount: number;
}

export const locationReviewMeta: LocationReviewMeta[] = ${JSON.stringify(locationMeta, null, 2)};

export const googleReviews: GoogleReview[] = ${JSON.stringify(allReviews, null, 2)};

export const combinedRating = ${weightedRating.toFixed(1)};
export const totalReviewCount = ${totalReviews};
`;

  const outPath = path.resolve(process.cwd(), 'src/data/googleReviews.ts');
  fs.writeFileSync(outPath, output);
  console.log(`\n📁 Saved to ${outPath}`);
  console.log(`   Combined: ${weightedRating.toFixed(1)}⭐ across ${totalReviews} reviews`);
}

main().catch(console.error);
